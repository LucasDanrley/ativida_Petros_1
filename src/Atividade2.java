public class Atividade2 {
    public static void main(String[] args) {
        int a  = 60;
        int b = 40;

        System.out.println("Valor de a: " + a);
        System.out.println("Valor de b: " + b);

        System.out.println();

        int soma = a + b;
        int subtracao = a - b;
        int multiplicacao = a * b;
        int divisao = a / b;

        System.out.println("Valor de soma: " + soma);
        System.out.println("Valor de subtracao: " + subtracao);
        System.out.println("Valor de multiplicacao: " + multiplicacao);
        System.out.println("Valor de divisao: " + divisao);
    }
}